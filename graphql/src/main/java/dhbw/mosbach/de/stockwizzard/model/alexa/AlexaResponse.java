package dhbw.mosbach.de.stockwizzard.model.alexa;

public class AlexaResponse {

    private String type;
    private String text;

    public AlexaResponse() {
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
